#include "libft.h"

char	*ft_strjoin_free(char *s1, char *s2)
{
	char	*str;

	str = ft_strjoin(s1, s2);
	ft_memdel((void **)&s1);
	ft_memdel((void **)&s2);
	return (str);
}
